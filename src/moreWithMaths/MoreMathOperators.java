package moreWithMaths;

public class MoreMathOperators {
	public static void main(String[] args) {
		//Declares three integers
		int num1, num2, ans;
		//Makes variables equal to a constant
		num1 = 10;
		num2 = 5;
		
		//Various mathematical Operations:
		
		ans = num1 + num2;
		System.out.print("+");
		System.out.println(ans);
		
		ans = num1- num2;
		System.out.print("-");
		System.out.println(ans);
		
		ans = num1 * num2;
		System.out.print("*");
		System.out.println(ans);
		
		ans = num1 / num2;
		System.out.print("/");
		System.out.println(ans);
	}

}

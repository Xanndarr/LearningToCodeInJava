package dealingWithVariables;

public class Variables {
	public static void main(String[] args) {
		//This declares "cat" as an integer
		int cat;
		//Makes "cat" equal to 25
		cat = 25;
		//Declares "dog" as a double
		double dog;
		//Makes "dog" equal to 35.7
		dog = 35.7;
		//Print variables and text onto the screen
		System.out.println(cat);
		System.out.print(dog);
		System.out.print("I just used some variables.");
	}

}

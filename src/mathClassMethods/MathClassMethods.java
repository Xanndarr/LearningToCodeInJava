package mathClassMethods;

public class MathClassMethods {
	public static void main(String[] args) {		
		//System.out.println(MathMethodGoesHere);
		//MathMethods:
		//Absolute: How much the number is away from 0, so always returns a positive value.
		Math.abs(26);
		//Rounds the number up regardless of value. Gives 8
		Math.ceil(7.4);
		//Rounds the number down regardless of value. Gives 7
		Math.floor(7.4);
		//Returns the largest number. Gives 8.6
		Math.max(8.6, 5.2);
		//Returns the smallest number. Gives 5.2
		Math.min(8.6, 5.2);
		//Power of... Gives 5^3 = 125
		Math.pow(5, 3);
		//Square Root. Gives 3.
		Math.sqrt(9);
	}
}
